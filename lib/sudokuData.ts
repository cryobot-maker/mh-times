export const SUDOKU_PUZZLES: SudokuPuzzle[] = [
  ...SUDOKU_EASY,
  ...SUDOKU_MEDIUM,
  ...SUDOKU_HARD,
];

function puzzlesForDifficulty(difficulty: SudokuDifficulty): SudokuPuzzle[] {
  switch (difficulty) {
    case "easy":
      return SUDOKU_EASY;
    case "medium":
      return SUDOKU_MEDIUM;
    case "hard":
      return SUDOKU_HARD;
  }
}

export function getPuzzle(
  difficulty: SudokuDifficulty,
  date: string
): SudokuPuzzle {
  const pool = puzzlesForDifficulty(difficulty);
  const exact = pool.find((p) => p.date === date);
  if (exact) return exact;
  const index = getDayNumber(date) % pool.length;
  return { ...pool[index], date };
}

export function parseDifficultyParam(
  value: string | null
): SudokuDifficulty | null {
  if (value === "easy" || value === "medium" || value === "hard") return value;
  return null;
}

