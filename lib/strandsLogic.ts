export async function loadStrandsValidWords(): Promise<Set<string>> {
  if (validWordsCache) return validWordsCache;
  const base = new Set(FALLBACK_WORDS);
  try {
    const res = await fetch("/valid-words.json");
    if (res.ok) {
      const json = (await res.json()) as string[];
      json.forEach((w) => {
        if (w.length >= 4) base.add(w.toUpperCase());
      });
    }
  } catch {
    // use fallback
  }
  validWordsCache = base;
  return base;
}

export function isNonThemeWord(
  word: string,
  puzzle: StrandsPuzzle
): boolean {
  const upper = word.toUpperCase();
  const themeSet = new Set(
    [...puzzle.themeWords, puzzle.spangram].map((w) => w.toUpperCase())
  );
  return !themeSet.has(upper) && upper.length >= 4;
}

export function buildShareGrid(
  foundTheme: string[],
  spangramFound: boolean,
  hintsUsed: number
): string {
  const lines = [
    `Strands #${foundTheme.length + (spangramFound ? 1 : 0)}`,
    `🟦 ${foundTheme.length} theme words`,
    spangramFound ? "🟨 Pangram found" : "",
    `Hints: ${hintsUsed}`,
  ].filter(Boolean);
  return lines.join("\n");
}

export function isGameComplete(
  puzzle: StrandsPuzzle,
  foundTheme: string[],
  spangramFound: boolean
): boolean {
  return (
    spangramFound &&
    foundTheme.length === puzzle.themeWords.length
  );
}
